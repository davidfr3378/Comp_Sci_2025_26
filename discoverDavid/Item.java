import java.util.ArrayList; 

public class Item{
    private String name;
    private String description;
    private String effect; 

    public Item(String name, String description, String effect){
        this.name = name;
        this.description = description;
        this.effect = effect;
    }

    //getters 
    public getName(){
        return name;
    }

    public getDescription(){
        return description;
    }

    public getEffect(){
        return name;
    }

}

